function dogAge(humanYears) {
    let calcAge = humanYears * 7;
    console.log(`Your dog's age is ${calcAge} years`);
    return calcAge;
}

//dog to human years

function humanAge(dogYears) {
    let calcAge = dogYears / 7;
    console.log(`Your human age is ${calcAge} years`);
    return calcAge;
}
dogAge(15);
humanAge(35);

